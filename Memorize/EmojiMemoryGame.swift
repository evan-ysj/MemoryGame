import SwiftUI

class EmojiMemoryGame: ObservableObject {
    @Published private var model: MemoryGame<String> = EmojiMemoryGame.createMyMemoryGame()
    
    private static func createMyMemoryGame() -> MemoryGame<String> {
        let emojis: Array<String> = ["ðŸ‘»", "ðŸŽƒ", "ðŸ•·"]
        return MemoryGame<String>(numberOfPairsOfCards: emojis.count) { i in emojis[i] }
    }
            
    
    // MARK: - Access to the Model
    
    var cards: Array<MemoryGame<String>.Card> {
        model.cards
    }
    
    // MARK: - Intent
    
    func choose(card: MemoryGame<String>.Card) {
        model.choose(card: card)
    }
    
    func resetGame() {
        model = EmojiMemoryGame.createMyMemoryGame()
    }
}
